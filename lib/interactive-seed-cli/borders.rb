    def self.print_all
        all_borders = {
            "Small" => @@small_borders,
            "Medium" => @@medium_borders,
            "Large" => @@large_borders
        }

       all_borders.each do |category, border_data|
        main_border = Borders.equals
        puts ""
        puts main_border
        puts main_border
        puts category
        puts ""

        border_data.each do |border_name, ascii_art|
            puts ""
            puts border_name
            puts ascii_art
            puts ""
        end
        puts main_border
        puts main_border
        puts ""
       end
